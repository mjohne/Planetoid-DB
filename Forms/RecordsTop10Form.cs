	/// <summary>Preselects an orbital element by display text when available.</summary>
	/// <param name="selectedElement">The element name to select, such as "Semi-major axis".</param>
	/// <remarks>Matching is case-insensitive and allows partial text matches.</remarks>
	private void PreselectOrbitalElement(string? selectedElement)
	{
		// If the provided selectedElement text is null, empty, or consists only of whitespace, or if the list box has no items, we default to selecting the first item (index 0) if nothing is currently selected. This ensures that there is always a valid selection when the form is initialized, even if the input for preselection is not valid.
		if (string.IsNullOrWhiteSpace(value: selectedElement) || listBox.Items.Count == 0)
		{
			// No valid preselection text or no items in the list box, default to selecting the first item if nothing is currently selected
			if (listBox.SelectedIndex < 0)
			{
				listBox.SelectedIndex = 0;
			}
			return;
		}
		// We attempt to find a matching item in the list box by comparing the display text of each item with the provided selectedElement text. The comparison is case-insensitive and allows for partial matches (i.e., if the item text contains the selectedElement text). If a match is found, we select that item; otherwise, we default to selecting the first item (index 0).
		int matchIndex = -1;
		for (int i = 0; i < listBox.Items.Count; i++)
		{
			// Get the display text of the current item, ensuring we handle null values gracefully by treating them as empty strings. This allows us to perform comparisons without risking null reference exceptions.
			string itemText = listBox.Items[index: i]?.ToString() ?? string.Empty;
			// Perform a case-insensitive comparison to check if the item text matches the selectedElement text exactly or contains it as a substring. If a match is found, we store the index and break out of the loop.
			if (itemText.Equals(value: selectedElement, comparisonType: StringComparison.OrdinalIgnoreCase) || itemText.Contains(value: selectedElement, comparisonType: StringComparison.OrdinalIgnoreCase))
			{
				// A match is found, store the index and exit the loop
				matchIndex = i;
				break;
			}
		}
		// Set the selected index to the found match or default to 0 if no match is found
		listBox.SelectedIndex = matchIndex >= 0 ? matchIndex : 0;
	}

