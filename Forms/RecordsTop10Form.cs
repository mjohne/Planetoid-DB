	/// <remarks>On success, the selected record is opened in the main form and this dialog closes.</remarks>
	private void NavigateToRank(int rankIndex)
	{
		// Validate the rank index to ensure it is within the valid range of 0 to 9, which corresponds to the top-ten ranks. If the index is out of range, we simply return without performing any navigation, as there is no valid record to navigate to for an invalid rank index.
		if (rankIndex < 0 || rankIndex >= topRecords.Count)
		{
			return;
		}
		// Attempt to find the main form (PlanetoidDbForm) among the open forms in the application. If the main form is not found, we show an error message to the user indicating that the main form is not available and return without performing any navigation. This ensures that we only attempt to navigate if the main form is accessible, preventing potential errors or crashes if the main form has been closed or is otherwise unavailable.
		if (Application.OpenForms.OfType<PlanetoidDbForm>().FirstOrDefault() is not PlanetoidDbForm mainForm)
		{
			ShowErrorMessage(message: "Main form is not available.");
