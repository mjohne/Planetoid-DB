	/// <summary>Handles the Click event of the Start control. Starts the main process.</summary>
	/// <param name="sender">The event source.</param>
	/// <param name="e">The <see cref="EventArgs"/> instance that contains the event data.</param>
	/// <remarks>This method is called when the Start control is clicked.</remarks>
	private async void Start_Click(object sender, EventArgs e)
	{
		// Validate that the planetoids database is loaded and that an orbital element is selected before starting the scan. If the database is empty, we show an error message to the user indicating that the MPCORB.DAT file was not found and return without starting the scan. If no orbital element is selected in the list box, we show an error message prompting the user to select an orbital element and return without starting the scan. This ensures that we have the necessary data and user input to perform a meaningful scan before we proceed.
		if (planetoidsDatabase.Count == 0)
		{
			ShowErrorMessage(message: I18nStrings.MpcorbDatNotFoundText);
			return;
		}
		// Check if an orbital element is selected in the list box. If not, show an error message prompting the user to select an orbital element and return without starting the scan. This ensures that we have a valid selection for the orbital element to rank by before we proceed with the scan.
		if (listBox.SelectedIndex < 0)
		{
			ShowErrorMessage(message: "Please select an orbital element first.");
			return;
		}
		// If we reach this point, it means that the database is loaded and an orbital element is selected, so we can proceed with starting the scan. We first cancel any existing scan if it is still running by calling Cancel on the cancellation token source, and then dispose of it to clean up resources. We create a new CancellationTokenSource for the new scan run. We reset the labels to clear any previous results from the UI, and we update the enabled state of the buttons and controls to reflect that a scan is now active (e.g., disable the Start button, enable the Cancel button, disable sort order buttons and list box to prevent changes during the scan). We also reset the progress bar to 0% and set its text accordingly.
		cancellationTokenSource?.Cancel();
		cancellationTokenSource?.Dispose();
		cancellationTokenSource = new CancellationTokenSource();
		ResetLabels();
		toolStripButtonStart.Enabled = false;
		toolStripButtonCancel.Enabled = true;
		toolStripButtonSortOrderAscending.Enabled = false;
		toolStripButtonSortOrderDescending.Enabled = false;
		listBox.Enabled = false;
		toolStripDropDownButtonSaveList.Enabled = false;
		contextMenuSaveToFile.Enabled = false;
		SetGotoButtonsEnabled(isEnabled: false);
		kryptonProgressBar.Value = 0;
		kryptonProgressBar.Text = "0 %";
		// Determine the active sort order based on the state of the sort order buttons. If the ascending button is checked, we set isAscending to true; if the descending button is checked, we set isAscending to false. This will be used to determine how we maintain the top-ten list during the scan, whether we are looking for the smallest values (ascending) or the largest values (descending) for the selected orbital element.
		bool isAscending = toolStripButtonSortOrderAscending.Checked;
		Progress<(int Percent, List<TopRecordEntry>? UpdatedEntries)> progress = new(handler: update => HandleProgressUpdate(percent: update.Percent, updatedEntries: update.UpdatedEntries));
		// Run the scan asynchronously and await its completion. We pass the selected orbital element index, the active sort order, the cancellation token for cooperative cancellation, and the progress callback to receive updates during the scan. If the scan completes successfully, we set the progress bar text to "100 %". If the scan is cancelled, we catch the OperationCanceledException and update the progress bar text to indicate that the scan was cancelled. If any other exceptions occur during the scan, we log an error with the exception details and update the progress bar text to show an error message. Finally, in the finally block, we reset the enabled state of the buttons and controls to allow for a new scan to be started, enable navigation if there are valid top-ten records, and dispose of the cancellation token source to clean up resources.
		try
		{
			await ScanTopRecordsAsync(
				selectedElementIndex: listBox.SelectedIndex,
				isAscending: isAscending,
				token: cancellationTokenSource.Token,
				progress: progress);
			kryptonProgressBar.Text = "100 %";
		}
		// Catch the OperationCanceledException to handle the case when the scan is cancelled by the user. We update the progress bar text to indicate that the scan was cancelled, providing feedback to the user that their cancellation request was acknowledged and processed.
		catch (OperationCanceledException)
		{
			kryptonProgressBar.Text = "Scan cancelled";
		}
		// Catch any other exceptions that may occur during the scan, log an error with the exception details, and update the progress bar text to show an error message. This ensures that we handle unexpected errors gracefully and provide feedback to the user about what went wrong.
		catch (Exception ex)
		{
			logger.Error(exception: ex, message: ex.Message);
			kryptonProgressBar.Text = I18nStrings.ErrorCaption;
		}
		// In the finally block, we reset the enabled state of the buttons and controls to allow for a new scan to be started, enable navigation if there are valid top-ten records, and dispose of the cancellation token source to clean up resources. This ensures that the UI is returned to a ready state for the user to start a new scan or navigate to records after the current scan has completed or been cancelled.
		finally
		{
			toolStripButtonStart.Enabled = true;
			toolStripButtonCancel.Enabled = false;
			toolStripButtonSortOrderAscending.Enabled = true;
			toolStripButtonSortOrderDescending.Enabled = true;
			listBox.Enabled = true;
			toolStripDropDownButtonSaveList.Enabled = true;
			contextMenuSaveToFile.Enabled = true;
			SetGotoButtonsEnabled(isEnabled: topRecords.Count > 0);
			cancellationTokenSource?.Dispose();
			cancellationTokenSource = null;
		}
	}

	/// <summary>Handles the Click event of the Cancel control. Cancels the main process.</summary>
	/// <param name="sender">The event source.</param>
	/// <param name="e">The <see cref="EventArgs"/> instance that contains the event data.</param>
	/// <remarks>This method is called when the Cancel control is clicked.</remarks>
	private void Cancel_Click(object sender, EventArgs e)
	{
