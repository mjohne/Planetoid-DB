	/// <summary>Runs the top-ten scan asynchronously for the selected orbital element.</summary>
	/// <param name="selectedElementIndex">Selected orbital element index.</param>
	/// <param name="isAscending">True for ascending (smallest values first), false for descending.</param>
	/// <param name="token">Cancellation token for cooperative cancellation.</param>
	/// <param name="progress">Progress callback for percent and live top-ten updates.</param>
	/// <returns>A task that completes when scanning is finished or cancelled.</returns>
	/// <remarks>The method runs the scan on a background thread to keep the UI responsive. It iterates through the planetoid records, extracts the relevant value for the selected element, and maintains a live top-ten list based on the active sort order. Progress updates are reported to update the progress bar and optionally refresh the displayed top-ten results as they are found.</remarks>
	private async Task ScanTopRecordsAsync(int selectedElementIndex, bool isAscending, CancellationToken token, IProgress<(int Percent, List<TopRecordEntry>? UpdatedEntries)> progress)
	{
		// Run the scan on a background thread to keep the UI responsive. The method iterates through the planetoid records, extracts the relevant value for the selected element, and maintains a live top-ten list based on the active sort order. Progress updates are reported to update the progress bar and optionally refresh the displayed top-ten results as they are found.
		await Task.Run(action: () =>
		{
			// Initialize a mutable list to hold the current top-ten entries as we scan through the records. This list will be updated dynamically as we evaluate each record and determine if it qualifies for the top-ten display based on the selected orbital element and active sort order.
			List<TopRecordEntry> currentTopEntries = [];
			int total = planetoidsDatabase.Count;
			int lastPercent = -1;
			// Iterate through each record in the planetoids database, checking for cancellation at each step to allow for cooperative cancellation of the scan. For each record, we attempt to parse it into a PlanetoidRecord and extract the relevant value for the selected orbital element. If the value is valid and qualifies for the top-ten list based on the active sort order, we update the current top-ten entries and report progress to update the UI with the latest results.
			for (int i = 0; i < total; i++)
			{
				token.ThrowIfCancellationRequested();
				int percent = (int)((i + 1) * 100L / total);
				// Attempt to parse the current record and extract the relevant value for the selected orbital element. If parsing fails or the value is invalid, we log a warning and skip to the next record. If the value is valid and qualifies for the top-ten list, we update the current top-ten entries and report progress to update the UI with the latest results.
				try
				{
					// Parse the raw line from the planetoids database into a PlanetoidRecord. If the DesignationName is null, empty, or consists only of whitespace, we skip this record as it does not have a valid designation to display in the top-ten list.
					PlanetoidRecord record = PlanetoidRecord.Parse(rawLine: planetoidsDatabase[index: i]);
					// If the DesignationName is null, empty, or consists only of whitespace, we skip this record as it does not have a valid designation to display in the top-ten list. This ensures that we only consider records with valid designations for inclusion in the top-ten results.
					if (string.IsNullOrWhiteSpace(value: record.DesignationName))
					{
						continue;
					}
					// Attempt to extract the selected orbital element's value from the record as both a string for display and a numeric value for comparison. If extraction and parsing succeed, we create a candidate TopRecordEntry and attempt to insert it into the current top-ten list based on the active sort order. If the candidate qualifies for the top-ten display, we report progress with the updated list of top entries to refresh the UI.
					if (TryGetSelectedElementValue(record: record, selectedElementIndex: selectedElementIndex, stringValue: out string? stringValue, numericValue: out double numericValue))
					{
						// Create a candidate TopRecordEntry with the designation, original string value, and numeric value for comparison. We then attempt to insert this candidate into the current top-ten list based on the active sort order. If the candidate qualifies for the top-ten display (i.e., it remains in the list after insertion), we report progress with the updated list of top entries to refresh the UI with the latest results.
						TopRecordEntry candidate = new(Designation: record.DesignationName, StringValue: stringValue, NumericValue: numericValue);
						if (TryInsertTopRecord(entries: currentTopEntries, candidate: candidate, isAscending: isAscending))
						{
							progress.Report(value: (percent, [.. currentTopEntries]));
						}
					}
				}
				// Catch any exceptions that may occur during parsing or value extraction, log a warning with the index of the record being processed and the exception message, and then continue to the next record. This ensures that a single malformed record does not disrupt the entire scanning process.
				catch (Exception ex)
				{
					// Log a warning if we fail to parse the record or extract the value, including the index of the record being processed and the exception message. This helps with debugging and allows us to identify any issues with specific records in the database without crashing the entire scan.
					logger.Warn(exception: ex, message: $"Skipping entry at index {i}: {ex.Message}");
				}
				// Report progress with the current percentage. We only report when the percentage changes to avoid excessive updates to the UI, which can cause performance issues. The progress report includes the current percentage and optionally the updated list of top entries if a new top record was found.
				if (percent != lastPercent)
				{
					progress.Report(value: (percent, null));
					lastPercent = percent;
				}
			}
		}, cancellationToken: token);
	}

	/// <summary>Updates the progress bar and top-ten labels from a progress callback.</summary>
	/// <param name="percent">Current scan percentage.</param>
	/// <param name="updatedEntries">Optional live top-ten snapshot.</param>
	/// <remarks>This method is called from the progress callback to update the progress bar and optionally refresh the displayed top-ten results as they are found during the scan. It checks if the percentage has changed before updating the progress bar to avoid unnecessary UI updates, and it applies any updated top-ten entries to the labels if provided.</remarks>
	private void HandleProgressUpdate(int percent, List<TopRecordEntry>? updatedEntries)
	{
		// Update the progress bar if the percentage has changed to avoid unnecessary UI updates. The progress bar's value is set to the current percentage, and the text is updated to show the percentage as well. Additionally, we update the taskbar progress indicator for this window to reflect the current scan progress.
		if (kryptonProgressBar.Value != percent)
		{
			kryptonProgressBar.Value = percent;
			kryptonProgressBar.Text = $"{percent} %";
			TaskbarProgress.SetValue(windowHandle: Handle, progressValue: (ulong)percent, progressMax: 100);
		}
		// If an updated list of top-ten entries is provided, we update the local topRecords list and apply it to the labels to refresh the displayed results. This allows the UI to show live updates of the top-ten records as they are found during the scan, providing immediate feedback to the user without waiting for the entire scan to complete.
		if (updatedEntries != null)
		{
			topRecords = updatedEntries;
			ApplyTopRecords(entries: topRecords);
		}
	}

	/// <summary>Navigates from a rank row to the corresponding planetoid in the main form.</summary>
