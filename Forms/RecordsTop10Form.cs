	/// <summary>Attempts to extract the currently selected orbital element as a numeric and display value.</summary>
	/// <param name="record">Parsed planetoid record.</param>
	/// <param name="selectedElementIndex">Selected orbital element index from the list box.</param>
	/// <param name="stringValue">Returns the original string representation for display.</param>
	/// <param name="numericValue">Returns the numeric value used for comparison.</param>
	/// <returns>True when extraction and parsing succeed; otherwise false.</returns>
	/// <remarks>The method maps the selected element index to the corresponding field in the record, retrieves the original string value for display, and attempts to parse it into a numeric value for ranking. It handles both integer and floating-point fields appropriately based on the selected element.</remarks>
	private static bool TryGetSelectedElementValue(PlanetoidRecord record, int selectedElementIndex, [NotNullWhen(returnValue: true)] out string? stringValue, out double numericValue)
	{
		// Map the selected element index to the corresponding field in the PlanetoidRecord and retrieve the original string value for display. The mapping is based on the order of elements in the list box, where each index corresponds to a specific orbital element. If the selected index does not match any known element, stringValue will be set to null.
		stringValue = selectedElementIndex switch
		{
			0 => record.MeanAnomaly,
			1 => record.ArgPeri,
			2 => record.LongAscNode,
			3 => record.Incl,
			4 => record.OrbEcc,
			5 => record.Motion,
			6 => record.SemiMajorAxis,
			7 => record.MagAbs,
			8 => record.SlopeParam,
			9 => record.NumberOpposition,
			10 => record.NumberObservation,
			11 => record.RmsResidual,
			_ => null
		};
		// If the retrieved string value is null, empty, or consists only of whitespace, we cannot parse it into a numeric value, so we set numericValue to 0 and return false to indicate that extraction and parsing did not succeed.
		if (string.IsNullOrWhiteSpace(value: stringValue))
		{
			numericValue = 0;
			return false;
		}
		// Determine if the selected element is an integer field (Number of Oppositions or Number of Observations) based on the selected index. This is important because we need to use different parsing logic for integer fields versus floating-point fields. The method uses int.TryParse for integer fields and double.TryParse for floating-point fields, both with invariant culture to ensure consistent parsing regardless of the user's locale settings.
		bool isIntegerField = selectedElementIndex is 9 or 10;
		if (isIntegerField && int.TryParse(s: stringValue, style: NumberStyles.Integer, provider: CultureInfo.InvariantCulture, result: out int intValue))
		{
			numericValue = intValue;
			return true;
		}
		// For non-integer fields, we attempt to parse the string value as a double. If parsing succeeds, we return true and set numericValue to the parsed double value. If parsing fails, we set numericValue to 0 and return false to indicate that extraction and parsing did not succeed.
		if (!isIntegerField && double.TryParse(s: stringValue, style: NumberStyles.Float, provider: CultureInfo.InvariantCulture, result: out double doubleValue))
		{
			numericValue = doubleValue;
			return true;
		}
		// If we reach this point, it means that parsing failed for the selected element, so we set numericValue to 0 and return false to indicate that extraction and parsing did not succeed.
		numericValue = 0;
		return false;
	}

	/// <summary>Inserts a candidate into the top-ten list when it qualifies for the active sort order.</summary>
	/// <param name="entries">Mutable list with current top-ten entries.</param>
	/// <param name="candidate">Candidate entry to evaluate.</param>
	/// <param name="isAscending">True for ascending (smallest first), false for descending (largest first).</param>
	/// <returns>True when the visible top-ten list changed; otherwise false.</returns>
	/// <remarks>The method adds the candidate to the list, sorts it according to the active sort order, and then trims it to maintain only the top ten entries. It returns true if the candidate remains in the list after this process, indicating that it qualifies for the top-ten display.</remarks>
	private static bool TryInsertTopRecord(List<TopRecordEntry> entries, TopRecordEntry candidate, bool isAscending)
	{
		// Add the candidate entry to the list of current top-ten entries and sort the list based on the active sort order. The sorting is done using a comparison that compares the NumericValue of the entries, either in ascending or descending order depending on the isAscending parameter. After sorting, we check if the list exceeds ten entries, and if so, we remove the excess entries to maintain only the top ten. Finally, we return true if the candidate entry remains in the list after this process, indicating that it qualifies for the top-ten display; otherwise, we return false.
		entries.Add(item: candidate);
		// Sort the entries based on the active sort order. If isAscending is true, we sort in ascending order (smallest values first); if false, we sort in descending order (largest values first). The comparison is done by comparing the NumericValue of the entries.
		entries.Sort(comparison: (left, right) => isAscending
			? left.NumericValue.CompareTo(value: right.NumericValue)
			: right.NumericValue.CompareTo(value: left.NumericValue));
		// If the list exceeds ten entries after adding the candidate and sorting, we remove the excess entries starting from index 10 to maintain only the top ten entries in the list. This ensures that the list always contains at most ten entries, which are the top records based on the active sort order.
		if (entries.Count > 10)
		{
			entries.RemoveRange(index: 10, count: entries.Count - 10);
		}
		// Return true if the candidate entry is still in the list after adding, sorting, and trimming to the top ten; otherwise, return false. This indicates whether the candidate qualifies for the top-ten display based on its numeric value and the active sort order.
		return entries.Contains(item: candidate);
	}

	/// <summary>Applies a top-ten snapshot to the table labels.</summary>
	/// <param name="entries">Ordered top-ten entries to display.</param>
	/// <remarks>Values are shown exactly as original source strings.</remarks>
	private void ApplyTopRecords(IReadOnlyList<TopRecordEntry> entries)
	{
		// Update the designation and value labels for each rank based on the provided top-ten entries. The method iterates through the labels and updates their text with the corresponding designation and string value from the entries list. If there are fewer than ten entries, the remaining labels are set to a default placeholder (e.g., "-") to indicate that no data is available for those ranks. This ensures that the UI accurately reflects the current top-ten records based on the latest scan results.
		KryptonLabel[] designationLabels = GetDesignationLabels();
		KryptonLabel[] valueLabels = GetValueLabels();
		for (int i = 0; i < designationLabels.Length; i++)
		{
			// Update the designation and value labels based on the corresponding entry in the top-ten entries list. If an entry exists for the current index, we set the labels to the entry's Designation and StringValue; otherwise, we set them to a default placeholder (e.g., "-").
			if (i < entries.Count)
			{
				// Set the designation label to the entry's Designation and the value label to the entry's StringValue for the current index. This updates the UI to reflect the current top-ten records based on the latest scan results.
				designationLabels[i].Values.Text = entries[index: i].Designation;
				valueLabels[i].Values.Text = entries[index: i].StringValue;
			}
			else
			{
				// No entry exists for this index, so we set the labels to a default placeholder (e.g., "-") to indicate that no data is available for this rank.
				designationLabels[i].Values.Text = "-";
				valueLabels[i].Values.Text = "-";
			}
		}
	}

	/// <summary>Runs the top-ten scan asynchronously for the selected orbital element.</summary>
	/// <param name="selectedElementIndex">Selected orbital element index.</param>
	/// <param name="isAscending">True for ascending (smallest values first), false for descending.</param>
	/// <param name="token">Cancellation token for cooperative cancellation.</param>
	/// <param name="progress">Progress callback for percent and live top-ten updates.</param>
	/// <returns>A task that completes when scanning is finished or cancelled.</returns>
	/// <remarks>The method runs the scan on a background thread to keep the UI responsive. It iterates through the planetoid records, extracts the relevant value for the selected element, and maintains a live top-ten list based on the active sort order. Progress updates are reported to update the progress bar and optionally refresh the displayed top-ten results as they are found.</remarks>
	private async Task ScanTopRecordsAsync(int selectedElementIndex, bool isAscending, CancellationToken token, IProgress<(int Percent, List<TopRecordEntry>? UpdatedEntries)> progress)
	{
		// Run the scan on a background thread to keep the UI responsive. The method iterates through the planetoid records, extracts the relevant value for the selected element, and maintains a live top-ten list based on the active sort order. Progress updates are reported to update the progress bar and optionally refresh the displayed top-ten results as they are found.
		await Task.Run(action: () =>
		{
			// Initialize a mutable list to hold the current top-ten entries as we scan through the records. This list will be updated dynamically as we evaluate each record and determine if it qualifies for the top-ten display based on the selected orbital element and active sort order.
			List<TopRecordEntry> currentTopEntries = [];
			int total = planetoidsDatabase.Count;
			int lastPercent = -1;
			// Iterate through each record in the planetoids database, checking for cancellation at each step to allow for cooperative cancellation of the scan. For each record, we attempt to parse it into a PlanetoidRecord and extract the relevant value for the selected orbital element. If the value is valid and qualifies for the top-ten list based on the active sort order, we update the current top-ten entries and report progress to update the UI with the latest results.
			for (int i = 0; i < total; i++)
			{
				token.ThrowIfCancellationRequested();
				int percent = (int)((i + 1) * 100L / total);
				// Attempt to parse the current record and extract the relevant value for the selected orbital element. If parsing fails or the value is invalid, we log a warning and skip to the next record. If the value is valid and qualifies for the top-ten list, we update the current top-ten entries and report progress to update the UI with the latest results.
				try
				{
					// Parse the raw line from the planetoids database into a PlanetoidRecord. If the DesignationName is null, empty, or consists only of whitespace, we skip this record as it does not have a valid designation to display in the top-ten list.
					PlanetoidRecord record = PlanetoidRecord.Parse(rawLine: planetoidsDatabase[index: i]);
					// If the DesignationName is null, empty, or consists only of whitespace, we skip this record as it does not have a valid designation to display in the top-ten list. This ensures that we only consider records with valid designations for inclusion in the top-ten results.
					if (string.IsNullOrWhiteSpace(value: record.DesignationName))
					{
						continue;
					}
					// Attempt to extract the selected orbital element's value from the record as both a string for display and a numeric value for comparison. If extraction and parsing succeed, we create a candidate TopRecordEntry and attempt to insert it into the current top-ten list based on the active sort order. If the candidate qualifies for the top-ten display, we report progress with the updated list of top entries to refresh the UI.
					if (TryGetSelectedElementValue(record: record, selectedElementIndex: selectedElementIndex, stringValue: out string? stringValue, numericValue: out double numericValue))
					{
						// Create a candidate TopRecordEntry with the designation, original string value, and numeric value for comparison. We then attempt to insert this candidate into the current top-ten list based on the active sort order. If the candidate qualifies for the top-ten display (i.e., it remains in the list after insertion), we report progress with the updated list of top entries to refresh the UI with the latest results.
						TopRecordEntry candidate = new(Designation: record.DesignationName, StringValue: stringValue, NumericValue: numericValue);
						if (TryInsertTopRecord(entries: currentTopEntries, candidate: candidate, isAscending: isAscending))
						{
							progress.Report(value: (percent, [.. currentTopEntries]));
						}
					}
				}
				// Catch any exceptions that may occur during parsing or value extraction, log a warning with the index of the record being processed and the exception message, and then continue to the next record. This ensures that a single malformed record does not disrupt the entire scanning process.
				catch (Exception ex)
				{
					// Log a warning if we fail to parse the record or extract the value, including the index of the record being processed and the exception message. This helps with debugging and allows us to identify any issues with specific records in the database without crashing the entire scan.
					logger.Warn(exception: ex, message: $"Skipping entry at index {i}: {ex.Message}");
				}
				// Report progress with the current percentage. We only report when the percentage changes to avoid excessive updates to the UI, which can cause performance issues. The progress report includes the current percentage and optionally the updated list of top entries if a new top record was found.
				if (percent != lastPercent)
				{
					progress.Report(value: (percent, null));
					lastPercent = percent;
				}
			}
		}, cancellationToken: token);
	}

	/// <summary>Updates the progress bar and top-ten labels from a progress callback.</summary>
	/// <param name="percent">Current scan percentage.</param>
	/// <param name="updatedEntries">Optional live top-ten snapshot.</param>
	/// <remarks>This method is called from the progress callback to update the progress bar and optionally refresh the displayed top-ten results as they are found during the scan. It checks if the percentage has changed before updating the progress bar to avoid unnecessary UI updates, and it applies any updated top-ten entries to the labels if provided.</remarks>
	private void HandleProgressUpdate(int percent, List<TopRecordEntry>? updatedEntries)
	{
		// Update the progress bar if the percentage has changed to avoid unnecessary UI updates. The progress bar's value is set to the current percentage, and the text is updated to show the percentage as well. Additionally, we update the taskbar progress indicator for this window to reflect the current scan progress.
		if (kryptonProgressBar.Value != percent)
		{
			kryptonProgressBar.Value = percent;
			kryptonProgressBar.Text = $"{percent} %";
			TaskbarProgress.SetValue(windowHandle: Handle, progressValue: (ulong)percent, progressMax: 100);
		}
		// If an updated list of top-ten entries is provided, we update the local topRecords list and apply it to the labels to refresh the displayed results. This allows the UI to show live updates of the top-ten records as they are found during the scan, providing immediate feedback to the user without waiting for the entire scan to complete.
		if (updatedEntries != null)
		{
			topRecords = updatedEntries;
			ApplyTopRecords(entries: topRecords);
		}
	}

	/// <summary>Navigates from a rank row to the corresponding planetoid in the main form.</summary>
	/// <param name="rankIndex">Zero-based rank index from 0 to 9.</param>
	/// <remarks>On success, the selected record is opened in the main form and this dialog closes.</remarks>
	private void NavigateToRank(int rankIndex)
	{
		// Validate the rank index to ensure it is within the valid range of 0 to 9, which corresponds to the top-ten ranks. If the index is out of range, we simply return without performing any navigation, as there is no valid record to navigate to for an invalid rank index.
		if (rankIndex < 0 || rankIndex >= topRecords.Count)
		{
			return;
		}
		// Attempt to find the main form (PlanetoidDbForm) among the open forms in the application. If the main form is not found, we show an error message to the user indicating that the main form is not available and return without performing any navigation. This ensures that we only attempt to navigate if the main form is accessible, preventing potential errors or crashes if the main form has been closed or is otherwise unavailable.
		if (Application.OpenForms.OfType<PlanetoidDbForm>().FirstOrDefault() is not PlanetoidDbForm mainForm)
		{
			ShowErrorMessage(message: "Main form is not available.");
			return;
		}
		// If the main form is found, we call the JumpToRecord method on the main form, passing an empty string for the index (since we are navigating by designation) and the designation of the selected top record. This will cause the main form to navigate to the corresponding planetoid record based on its designation. After invoking the navigation, we bring the main form to the front to ensure it is visible to the user, set the dialog result to OK to indicate a successful navigation, and then close this dialog.
		mainForm.JumpToRecord(index: string.Empty, designation: topRecords[index: rankIndex].Designation);
		mainForm.BringToFront();
		DialogResult = DialogResult.OK;
		Close();
	}

