	/// <summary>Attempts to extract the currently selected orbital element as a numeric and display value.</summary>
	/// <param name="record">Parsed planetoid record.</param>
	/// <param name="selectedElementIndex">Selected orbital element index from the list box.</param>
	/// <param name="stringValue">Returns the original string representation for display.</param>
	/// <param name="numericValue">Returns the numeric value used for comparison.</param>
	/// <returns>True when extraction and parsing succeed; otherwise false.</returns>
	/// <remarks>The method maps the selected element index to the corresponding field in the record, retrieves the original string value for display, and attempts to parse it into a numeric value for ranking. It handles both integer and floating-point fields appropriately based on the selected element.</remarks>
	private static bool TryGetSelectedElementValue(PlanetoidRecord record, int selectedElementIndex, [NotNullWhen(returnValue: true)] out string? stringValue, out double numericValue)
	{
		// Map the selected element index to the corresponding field in the PlanetoidRecord and retrieve the original string value for display. The mapping is based on the order of elements in the list box, where each index corresponds to a specific orbital element. If the selected index does not match any known element, stringValue will be set to null.
		stringValue = selectedElementIndex switch
		{
			0 => record.MeanAnomaly,
			1 => record.ArgPeri,
			2 => record.LongAscNode,
			3 => record.Incl,
			4 => record.OrbEcc,
			5 => record.Motion,
			6 => record.SemiMajorAxis,
			7 => record.MagAbs,
			8 => record.SlopeParam,
			9 => record.NumberOpposition,
			10 => record.NumberObservation,
			11 => record.RmsResidual,
			_ => null
		};
		// If the retrieved string value is null, empty, or consists only of whitespace, we cannot parse it into a numeric value, so we set numericValue to 0 and return false to indicate that extraction and parsing did not succeed.
		if (string.IsNullOrWhiteSpace(value: stringValue))
		{
			numericValue = 0;
			return false;
		}
		// Determine if the selected element is an integer field (Number of Oppositions or Number of Observations) based on the selected index. This is important because we need to use different parsing logic for integer fields versus floating-point fields. The method uses int.TryParse for integer fields and double.TryParse for floating-point fields, both with invariant culture to ensure consistent parsing regardless of the user's locale settings.
		bool isIntegerField = selectedElementIndex is 9 or 10;
		if (isIntegerField && int.TryParse(s: stringValue, style: NumberStyles.Integer, provider: CultureInfo.InvariantCulture, result: out int intValue))
		{
			numericValue = intValue;
			return true;
		}
		// For non-integer fields, we attempt to parse the string value as a double. If parsing succeeds, we return true and set numericValue to the parsed double value. If parsing fails, we set numericValue to 0 and return false to indicate that extraction and parsing did not succeed.
		if (!isIntegerField && double.TryParse(s: stringValue, style: NumberStyles.Float, provider: CultureInfo.InvariantCulture, result: out double doubleValue))
		{
			numericValue = doubleValue;
			return true;
		}
		// If we reach this point, it means that parsing failed for the selected element, so we set numericValue to 0 and return false to indicate that extraction and parsing did not succeed.
		numericValue = 0;
		return false;
	}

	/// <summary>Inserts a candidate into the top-ten list when it qualifies for the active sort order.</summary>
	/// <param name="entries">Mutable list with current top-ten entries.</param>
	/// <param name="candidate">Candidate entry to evaluate.</param>
	/// <param name="isAscending">True for ascending (smallest first), false for descending (largest first).</param>
	/// <returns>True when the visible top-ten list changed; otherwise false.</returns>
	/// <remarks>The method adds the candidate to the list, sorts it according to the active sort order, and then trims it to maintain only the top ten entries. It returns true if the candidate remains in the list after this process, indicating that it qualifies for the top-ten display.</remarks>
	private static bool TryInsertTopRecord(List<TopRecordEntry> entries, TopRecordEntry candidate, bool isAscending)
	{
		// Add the candidate entry to the list of current top-ten entries and sort the list based on the active sort order. The sorting is done using a comparison that compares the NumericValue of the entries, either in ascending or descending order depending on the isAscending parameter. After sorting, we check if the list exceeds ten entries, and if so, we remove the excess entries to maintain only the top ten. Finally, we return true if the candidate entry remains in the list after this process, indicating that it qualifies for the top-ten display; otherwise, we return false.
		entries.Add(item: candidate);
		// Sort the entries based on the active sort order. If isAscending is true, we sort in ascending order (smallest values first); if false, we sort in descending order (largest values first). The comparison is done by comparing the NumericValue of the entries.
		entries.Sort(comparison: (left, right) => isAscending
			? left.NumericValue.CompareTo(value: right.NumericValue)
			: right.NumericValue.CompareTo(value: left.NumericValue));
		// If the list exceeds ten entries after adding the candidate and sorting, we remove the excess entries starting from index 10 to maintain only the top ten entries in the list. This ensures that the list always contains at most ten entries, which are the top records based on the active sort order.
		if (entries.Count > 10)
		{
			entries.RemoveRange(index: 10, count: entries.Count - 10);
		}
		// Return true if the candidate entry is still in the list after adding, sorting, and trimming to the top ten; otherwise, return false. This indicates whether the candidate qualifies for the top-ten display based on its numeric value and the active sort order.
		return entries.Contains(item: candidate);
	}

